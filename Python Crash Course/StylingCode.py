#Because code is read more than written you should make code easy to read

#Indentation:
#Use four spaces per indentation level, in an IDE tabs are usually converted into 4 spaces, but make sure it does so

#Line length:
#Make sure programming lines are less than 80 characters, and comments are less than 72

#Blank Lines:
#Usually it comes natural. Seperate blocks or independent sections of code by at least one blank line, but less than 3

#You can look for more guidlines following the PEP 8 protocol
#https://python.org/dev/peps/pep-0008/
